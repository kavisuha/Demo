public class MinMax {
	public static void main(String[] args){
		Byte min1 = Byte.MIN_VALUE;
		Byte max1 = Byte.MAX_VALUE;
		System.out.println("Byte range:");
		System.out.println("min:"+min1);
		System.out.println("max:"+max1);
		Short min2 = Short.MAX_VALUE;
		Short max2 = Short.MIN_VALUE;
		System.out.println("Short range:");
		System.out.println("min:"+min2);
		System.out.println("max:"+max2);
		Integer max3 = Integer.MAX_VALUE;
		Integer min3 = Integer.MIN_VALUE;
		System.out.println("Integer range:");
		System.out.println("min:"+max3);
		System.out.println("max:"+min3 );
		Long min4 = Long.MAX_VALUE;
		Long max4 = Long.MIN_VALUE;
		System.out.println("Float range:");
		System.out.println("min:"+min4);
		System.out.println("max:"+max4);
		Float max5 = Float.MAX_VALUE;
		Float min5 = Float.MIN_VALUE;
		System.out.println("Float range:");
		System.out.println("min:"+min5);
		System.out.println("max:"+max5);
		Double min6 = Double.MAX_VALUE;
		Double max6 = Double.MIN_VALUE;
		System.out.println("Double range:");
		System.out.println("min:"+min6);
		System.out.println("max:"+max6);
	}
}